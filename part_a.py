def interactive_mode(system: AdmissionSystem):
    """Allow user to input custom values."""
    print("\nEnter student details (or 'quit' to exit):")

    while True:
        try:
            score = input("\nEntrance Score (0-100): ").strip()
            if score.lower() == 'quit':
                break

            gpa = input("GPA (0-10): ").strip()
            rec = input("Recommendation (yes/no): ").strip()
            cat = input("Category (general/obc/sc_st): ").strip()
            extra = input("Extracurricular Score (0-10): ").strip()

            app = StudentApplication(
                entrance_score=float(score),
                gpa=float(gpa),
                has_recommendation=rec,
                category=cat,
                extracurricular_score=float(extra)
            )

            result = system.evaluate(app)
            print(format_output(result))

        except ValueError as e:
            print(f"Input error: Please enter valid numbers. ({e})")
        except Exception as e:
            print(f"Error: {e}")


